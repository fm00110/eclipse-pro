package 哈希表;

public class Info2 {

	private String key;
	private String name;
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Info2(String key, String name) {
		super();
		this.key = key;
		this.name = name;
	}
	
}
